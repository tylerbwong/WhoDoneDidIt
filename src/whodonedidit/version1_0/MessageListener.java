package whodonedidit.version1_0;

/**
 * MessageListener class is implemented by classes that want to listen
 * to messages from the server and client.
 *
 * @author Markus
 * @version 0.0
 * @since 25-Oct-2015
 */
public interface MessageListener
{

   /**
    * This will be used to handle messages sent between server and
    * client.
    *
    * @param msg An object that contains important information
    */
   public abstract void receiveMessage(Object msg);
}
